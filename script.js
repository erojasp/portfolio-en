const themeToggle = document.getElementById("themeToggle");
const langEnBtn = document.getElementById("langEn");
const langEsBtn = document.getElementById("langEs");
const navLinks = document.querySelectorAll(".nav a");
const translatable = document.querySelectorAll("[data-i18n]");

function applyLanguage(lang){
  const dict = translations[lang] || translations.en;
  document.documentElement.lang = dict.pageLang;

  translatable.forEach(el => {
    const key = el.dataset.i18n;
    if (dict[key] !== undefined) {
      el.innerHTML = dict[key];
    }
  });

  const navMap = {
    profile: dict.navProfile,
    experience: dict.navExperience,
    highlights: dict.navHighlights,
    skills: dict.navSkills,
    projects: dict.navProjects,
    languages: dict.navLanguages
  };

  navLinks.forEach(link => {
    const key = link.dataset.nav;
    if (navMap[key]) link.textContent = navMap[key];
  });

  langEnBtn.classList.toggle("is-active", lang === "en");
  langEsBtn.classList.toggle("is-active", lang === "es");

  localStorage.setItem("lang", lang);
  updateThemeButton();
}

function applyTheme(theme){
  const isLight = theme === "light";
  document.body.classList.toggle("light", isLight);
  document.querySelector('meta[name="theme-color"]').setAttribute("content", isLight ? "#f3f6fb" : "#0b1220");
  localStorage.setItem("theme", theme);
  updateThemeButton();
}

function updateThemeButton(){
  const lang = localStorage.getItem("lang") || "en";
  const theme = localStorage.getItem("theme") || "dark";
  const dict = translations[lang] || translations.en;
  themeToggle.textContent = theme === "light" ? dict.themeButtonDark : dict.themeButtonLight;
}

function detectInitialTheme(){
  const storedTheme = localStorage.getItem("theme");
  if (storedTheme) return storedTheme;
  return window.matchMedia("(prefers-color-scheme: light)").matches ? "light" : "dark";
}

function setActiveNav(){
  let current = "profile";
  navLinks.forEach(link => {
    const id = link.getAttribute("href").slice(1);
    const section = document.getElementById(id);
    if (!section) return;
    const top = section.getBoundingClientRect().top;
    if (top <= 120) current = id;
  });

  navLinks.forEach(link => {
    const active = link.dataset.nav === current;
    link.classList.toggle("active", active);
    link.setAttribute("aria-current", active ? "page" : "false");
  });
}

themeToggle.addEventListener("click", () => {
  const current = localStorage.getItem("theme") || "dark";
  applyTheme(current === "dark" ? "light" : "dark");
});

langEnBtn.addEventListener("click", () => applyLanguage("en"));
langEsBtn.addEventListener("click", () => applyLanguage("es"));
window.addEventListener("scroll", setActiveNav);

applyTheme(detectInitialTheme());
applyLanguage(localStorage.getItem("lang") || "en");
setActiveNav();
